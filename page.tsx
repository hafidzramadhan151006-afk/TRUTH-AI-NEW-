import { useState } from "react";

export default function Home() {
  const [question, setQuestion] = useState("");
  const [answer, setAnswer] = useState("");

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
     const res = await fetch("/api/ask", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ question }),
    });

    const data = await res.json();
    setAnswer(data.answer);
  };

  return (
    <main className="flex flex-col items-center justify-center min-h-screen p-6">
      <h1 className="text-2xl font-bold mb-4">Tanya ke GPT</h1>

      <form
        onSubmit={handleSubmit}
        className="flex flex-col w-full max-w-md gap-4"
      >
        <textarea
          value={question}
          onChange={(e) => setQuestion(e.target.value)}
          placeholder="Tulis pertanyaan kamu di sini..."
          className="border rounded-lg p-2 w-full"
          rows={4}
        />
        <button
          type="submit"
          className="bg-blue-500 text-white rounded-lg px-4 py-2 hover:bg-blue-600"
        >
          Kirim
        </button>
      </form>

      {answer && (
        <div className="mt-6 p-4 border rounded-lg w-full max-w-md bg-gray-50">
          <h2 className="font-semibold mb-2">Jawaban:</h2>
          <p>{answer}</p>
        </div>
      )}
    </main>
  );
}
